def fix_mojibake_in_content(content):
    """Исправляет кракозябры в контенте"""
    for pattern, replacement in MOJIBAKE_REPLACEMENTS:
        content = re.sub(pattern, replacement, content)
    return content

def fix_file(filepath):
    """Исправляет файл"""
    try:
        with open(filepath, 'r', encoding='utf-8', errors='replace') as f:
            content = f.read()
        
        original = content
        content = fix_mojibake_in_content(content)
        
        if content != original:
            with open(filepath, 'w', encoding='utf-8', newline='') as f:
                f.write(content)
            return True
        return False
    except Exception as e:
        print(f"Ошибка {filepath}: {e}")
        return False

def main():
    html_files = glob.glob('**/*.html', recursive=True)
    
    fixed = 0
    print("Исправление кракозябр...")
    for filepath in sorted(html_files):
        if fix_file(filepath):
            fixed += 1
            if fixed % 20 == 0:
                print(f"Исправлено: {fixed}...")
    
    print(f"Всего исправлено: {fixed}")

if __name__ == '__main__':
    main()

